# -*- coding: utf-8 -*-
"""
This module defines add function.

Example:
    print(add(1.11, 2.22) == 1.11 + 2.22)

"""


def add(a: float, b: float) -> float:
    """type-annotated function add.

    Args:
        a (float): first arg.
        b (float): second arg.

    Returns:
        their sum.

    """
    return a + b
